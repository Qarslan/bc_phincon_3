function plus(a, b) {
    return a + b;
}

function times(c, d) {
    return c * d;
}

function minus(e, f) {
    return e - f;
}

function divided(g, h) {
    return g / h;
}

let resultPlus = plus(1, 2);
let resultTimes = times(2, 2);
let resultMinus = minus(2, 2);
let resultDivided = divided(4, 2);

console.log(resultPlus)
console.log(resultTimes)
console.log(resultMinus)
console.log(resultDivided)